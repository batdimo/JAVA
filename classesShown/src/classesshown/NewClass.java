public class NewClass {
    private   int year;
    private String name;
    
    public NewClass(int year, String name) {
        this.setYear(year);
        this.setName(name);
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

   
    
    
}
