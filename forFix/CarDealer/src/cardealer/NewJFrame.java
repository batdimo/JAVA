    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Car a1  = new Car();
        a1.setMake(jTextField1.getText());
        a1.setModel(jTextField2.getText());
        a1.setYear(Integer.parseInt(jTextField3.getText()));

        try  {
            double price = Double.parseDouble(jTextField4.getText());           
             if(price < 0) {
                throw new InvalidPriceException(
                        "The price must be > 0");
            }
            a1.setPrice(price);        
        } catch (NumberFormatException n) {
            jTextField4.setBackground(Color.red);
            JOptionPane.showMessageDialog(
                    this, "Greshka pri preobrazuvaneto"
                    + " " + jTextField4.getText());
            return;        
        } catch (InvalidPriceException n) {
            jTextField4.setBackground(Color.red);
            JOptionPane.showMessageDialog(
                    this, n.getMessage()+ jTextField4.getText());
            return;
        }

        if(jButton1.getText().equals("Update")) {
            model.getCars()[model.getUpdatePosition()] = a1;
            jTextArea1.setText("Car updated " + a1);          
            model.setUpdatePosition(-1);
            jButton1.setText("Add");
        } else {
            model.add(a1);
            jTextArea1.setText("New car added " + a1.getMake());          
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        if (model.getPosition() == 0) {
            jTextArea1.setText("EMPTY DATA SET" );
            return;
        } else {
            jTextArea1.setText("CAR LISTING" 
                    + String.format("(%d)", 
                            model.getPosition())
                    + "\n-----------------------------" );            
        }
        displayCars(model.getCars());
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        // clear text area
        jTextArea1.setText("Searching\n-----------------------\n");
        
        // get input - Make
        String make = jTextField1.getText();
        jTextArea1.append("Make: " + make+"\n-----------------------\n");
        
        // search by make
        Car [] cars = model.searchByMake(make);
        
        // display results
        displayCars(cars);
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
         // 1. select car
        String index = JOptionPane.
                showInputDialog(this, "Enter car ID");
        JOptionPane.showMessageDialog(this, 
                "Car  " + index);
        
        // 2. fill car details
        Car c = model.getCars()[Integer.parseInt(index)];
         jTextField1.setText(c.getMake());       
         jTextField2.setText(c.getModel());       
         jTextField3.setText("" + c.getYear());       
         jTextField4.setText("" + c.getPrice());       
         
         // 3. update car       
         jButton1.setText("Update"); 
         model.setUpdatePosition(Integer.parseInt(index));
         
         // 4. show info
    }//GEN-LAST:event_jButton4ActionPerformed

