    private void displayCars(Car [] cars) {
        for (int i = 0;   cars != null && i < cars.length; i++) {            
            Car currentCar = cars[i];                    
            if(currentCar != null) {
                jTextArea1.append("\ncat item " 
                        + currentCar.getMake()
                        + currentCar.getModel()
                        + currentCar.getPrice()
                );
            }
        }
    }
